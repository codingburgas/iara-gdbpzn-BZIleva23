def seed():
    with app.app_context():
        db.create_all()

        # ── Admin потребител ──────────────────────────────────────────
        if not User.query.filter_by(username='admin').first():
            db.session.add(User(username='admin', password='admin123', role='admin'))
            print("✅ Admin създаден: admin / admin123")
        else:
            admin = User.query.filter_by(username='admin').first()
            admin.role = 'admin'
            admin.password = 'admin123'
            print("✅ Admin обновен: admin / admin123")

        # ── Пожарни коли ─────────────────────────────────────────────
        if FireVehicle.query.count() == 0:
            for cs, vt, mo, reg, st, wc in VEHICLES:
                db.session.add(FireVehicle(
                    call_sign=cs, vehicle_type=vt, model=mo,
                    region=reg, station=st, water_cap_l=wc
                ))
            db.session.commit()
            print(f"✅ {len(VEHICLES)} пожарни автомобила добавени")
        else:
            print(f"ℹ️  Автомобили вече съществуват ({FireVehicle.query.count()} бр.)")

        db.session.commit()

        # ── Резюме ───────────────────────────────────────────────────
        print("\n📋 Резюме:")
        print(f"   Потребители: {User.query.count()}")
        print(f"   Автомобили:  {FireVehicle.query.count()}")
        print(f"\n🚀 Влез на: http://127.0.0.1:5000  →  admin / admin123")


