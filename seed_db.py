def run_seeding():
    with app.app_context():
        print("[PHOENIX DB] Изчистване и преналиване на националната база...")
        db.drop_all()
        db.create_all()

        admin_user = User(username='admin', password='admin123', role='admin')
        db.session.add(admin_user)

        for call_sign, v_type, model, region, station, water in VEHICLES_DATABASE:
            vehicle = FireVehicle(
                call_sign=call_sign, vehicle_type=v_type, model=model,
                region=region, station=station, status='available', water_cap_l=water
            )
            db.session.add(vehicle)

        db.session.commit()
        print(f"✅ Базата е готова с {len(VEHICLES_DATABASE)} автентични автомобила!")


if __name__ == '__main__':
    run_seeding()